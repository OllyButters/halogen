<ul>
   <li>Nothing seems to work! - Do you have Javascipt enabled?</li>
   <li>Where can I find out some of the more technical information? - Have a read of the <a href="http://www2.le.ac.uk/offices/itservices/resources/cs/pso/project-websites/halogen/documents/HALOGEN-Data-glossary-V1-final.pdf">data glossary</a>.</li>
   <li>I have an issue that is not covered here or in the <a href="../guide/">guide</a> - Head over to the <a href="../contact/">contact us page</a> and drop us an email.</li>
   <li>This all looks very similar to the <a href="http://kepn.halogen.le.ac.uk">kepn.halogen.le.ac.uk</a> stuff, is it? - Yes, kepn.halogen.le.ac.uk has the Key to English Place-Names only, while the rest of the halogen.le.ac.uk pages have all the data sets.</li>
  <li>When I zoom in on the query maps there are gaps between some of the counties - The county outlines are there as a rough guide, so if you click on e.g. Norfolk then all of Norfolk will be included in your query, even if it is just outside of the rough outline.</li>
